package Lesson1;

public class Person {

    private String name;
    private int runningSpeed;

    public Person(String name, int runningSpeed) {
        this.name = name;
        this.runningSpeed = runningSpeed;
    }

    public String getName() {
        return name;
    }

    public int getRunningSpeed() {
        return runningSpeed;
    }
}


